class loop{
    public static void main(String[] args) {
        // looop statment -- for, while, do-while
        
        // for loop
        for(int i = 100; i >= 1; i--){
            System.out.println(i);
        }


        //while loop
        int j = 100;
        while(j >= 1){
            System.out.println(j);
            j--;
        }


        //do-while
        int k = 100;
        do{
            System.out.println(k);
            k--;
        }while(k >= 1);

    }
}