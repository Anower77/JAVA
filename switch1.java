class switch1{

    public static void main(String[] args) {
        // conditional statment -- switch
        int day = 4;
        switch (day) {
            case 1:
                System.out.println("Sat");
                break;
            case 2:
                System.out.println("Sun");
                break;
            default:
                System.out.println("Mon - wed");
            
        }
    }
}